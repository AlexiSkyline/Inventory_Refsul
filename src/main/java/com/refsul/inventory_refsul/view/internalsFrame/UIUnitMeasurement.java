    public boolean isValidInput()
    {
        ItemForm description = new ItemForm( "Descripción", this.jTextDescription.getText() )
                .addValidador( new RequiredValidator() )
                .addValidador( new LengthValidator( 3, 20 ));

        if( !description.isValid() ) {
            description.printMessage();
            this.jTextDescription.requestFocus();
            return false;
        }

        return true;
    }

    private void buttonAddActionPerformed( java.awt.event.ActionEvent evt ) throws SQLException
    {
        this.idUnitMeasurement = 0;
        this.buildUnitMeasurement();

        if( this.isValidInput() ) {
            if( this.unitMeasurementController.createUnitMeasurement( this.unitMeasurement ) ) {
                this.completeAddAction();
                JOptionPane.showMessageDialog( null, "Método de pago agregado correctamente",
                        "Método de pago agregado", JOptionPane.INFORMATION_MESSAGE );
            } else {
                JOptionPane.showMessageDialog( null, "Error al agregar nuevo Método de pago",
                        "Error al agregar", JOptionPane.WARNING_MESSAGE );
            }
        }
    }

    public void showListUnitMeasurements() throws SQLException
    {
        List<UnitMeasurement> listUnitMeasurements = this.unitMeasurementController.getUnitMeasurements();
        this.tableModel = ( DefaultTableModel ) this.tableUnitMeasurement.getModel();

        for( UnitMeasurement unitMeasurement : listUnitMeasurements )
        {
            this.tableModel.addRow( new Object[]{ unitMeasurement.getIdUnitMeasurement(), unitMeasurement.getDescription() } );
        }
    }

    private void tableUnitMeasurementMouseClicked( java.awt.event.MouseEvent evt )
    {
        this.disableButtonAdd();
        this.activeButtonUpdateAndDelete();

        int selectedItem = this.tableUnitMeasurement.rowAtPoint( evt.getPoint() );
        this.idUnitMeasurement = Integer.parseInt( this.tableUnitMeasurement.getValueAt( selectedItem, 0 ) + "" );
        this.jTextDescription.setText( this.tableUnitMeasurement.getValueAt( selectedItem, 1 ) + "" );
    }

    private void buttonUpdateActionPerformed( java.awt.event.ActionEvent evt ) throws SQLException
    {
        this.buildUnitMeasurement();

        if( this.isValidInput() ) {
            int confirm = JOptionPane.showConfirmDialog( null, "¿Desea guardar cambios?\n" +
                            "Los datos del Método de pago se modificaran" , "Actualizando Método de pago",
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE );

            if( confirm == 0 )
            {
                if( this.unitMeasurementController.updateUnitMeasurement( this.unitMeasurement ) ) {
                    this.completeDeleteOrUpdateAction();
                    JOptionPane.showMessageDialog( null, "Método de pago actualizado correctamente",
                            "Método de pago actualizado", JOptionPane.INFORMATION_MESSAGE );
                } else {
                    JOptionPane.showMessageDialog( null, "Error al actualizar Método de pago",
                            "Error al actualizar", JOptionPane.WARNING_MESSAGE );
                }
            }
        }
    }

    private void buttonDeleteActionPerformed( java.awt.event.ActionEvent evt ) throws SQLException
    {
        int confirm = JOptionPane.showConfirmDialog( null, "¿Desea eliminar el Método de pago?\n" +
                        "El Método de pago se eliminará permanentemente" , "Eliminar Método de pago",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE );

        if( confirm == 0 ) {
            this.unitMeasurementController.deleteUnitMeasurement( this.idUnitMeasurement );
            this.completeDeleteOrUpdateAction();
            JOptionPane.showMessageDialog( null, "Método de pago se elimino correctamente",
                    "Método de pago eliminado", JOptionPane.INFORMATION_MESSAGE );
        }
    }

    private void buttonCleanInputActionPerformed( java.awt.event.ActionEvent evt )
    {
        this.cleanFormInput();
        this.disableButtonUpdateAndDelete();
        this.activeButtonAdd();
    }
    
    public void cleanListUnitMeasurements()
    {
        this.tableModel = ( DefaultTableModel ) this.tableUnitMeasurement.getModel();
        for( int i = this.tableModel.getRowCount() - 1 ; i >= 0 ; i-- )
        {
            this.tableModel.removeRow( i );
        }
    }

    private void cleanFormInput()
    {
        this.jTextDescription.setText( null );
    }

    private void disableButtonAdd()
    {
        this.buttonAdd.setEnabled( false );
    }

    private void disableButtonUpdateAndDelete()
    {
        this.buttonUpdate.setEnabled( false );
        this.buttonDelete.setEnabled( false );
    }

    private void activeButtonAdd()
    {
        this.buttonAdd.setEnabled( true );
    }

    private void activeButtonUpdateAndDelete()
    {
        this.buttonUpdate.setEnabled( true );
        this.buttonDelete.setEnabled( true );
    }

    private void completeAddAction() throws SQLException
    {
        this.cleanListUnitMeasurements();
        this.showListUnitMeasurements();
        this.cleanFormInput();
    }
    private void completeDeleteOrUpdateAction() throws SQLException
    {
        this.cleanListUnitMeasurements();
        this.cleanFormInput();
        this.showListUnitMeasurements();
        this.disableButtonUpdateAndDelete();
        this.activeButtonAdd();
    }

    private void buildUnitMeasurement() {
        this.unitMeasurement.setIdUnitMeasurement( this.idUnitMeasurement );
        this.unitMeasurement.setDescription( jTextDescription.getText() );
    }

